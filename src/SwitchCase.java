
public class SwitchCase {

	public static void main(String[] args) {
		char character='A';
		
		switch (character) {
		case 'k':
		System.out.println("this is a vowel");
		break;
		case 'E':
			System.out.println("this is a vowel");
			break;
		case 'i':
			System.out.println("this is a vowel");
			break;
		case 'o':
			System.out.println("this is a vowel");
			break;
		case 'u':
			System.out.println("this is a vowel");
			break;
		default:
			System.out.println("this is not a vowel.");
			break;                              
		}
	}

}
